package com.gtech.foodipsdelivery.api;

public class DataModelClass {

    //INITIALIZING LOGIN DATA
    String username;
    String password;

    private DataModelClass() {

        //CONSTRUCTOR FOR LOGIN DATA
        this.username     = username;
        this.password     = password;
    }

    //GETTERS AND SETTERS FOR LOGIN DATA

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }
}
